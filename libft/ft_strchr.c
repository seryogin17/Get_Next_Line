#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == '\0')
		return ((char*)s + ft_strlen(s));
	while (*s && *s != c)
		s++;
	if (*s)
		return ((char*)s);
	return (NULL);
}
