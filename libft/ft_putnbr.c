#include "libft.h"

void	ft_putnbr(int n)
{
	long	num;

	num = (long)n;
	if (num < 0)
	{
		num = -num;
		ft_putchar('-');
	}
	if (num / 10)
		ft_putnbr(num / 10);
	ft_putchar('0' + num % 10);
}
